#include <libft.h>

size_t	ft_nbrlen(int nbr)
{
	size_t	len;

	len = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
		nbr = nbr * (-1);
	while (nbr > 0)
	{
		nbr = nbr / 10;
		++len;
	}
	return (len);
}
