#include <libft.h>

void	ft_tabdel(char **tab)
{
	int	i;

	i = (-1);
	if (tab != NULL)
	{
		while (tab[++i] != NULL)
			ft_strdel(&(tab[i]));
		free((char **)tab);
		tab = NULL;
	}
}
